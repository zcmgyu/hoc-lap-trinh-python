# Python Comparision Operators

## Equal Operator

x1 = 10
y1 = 10

print("x1 == y1 = ", x1 == y1)

## Not Equal Operator

x2 = 10
y2 = 20

print("x2 != y2 = ", x2 != y2)

## Greater Than Operator

x3 = 10
y3 = 20

print("x3 > y3 = ", x3 > y3)

## Less Than Operator

x4 = 10
y4 = 20

print("x4 < y4 = ", x4 < y4)

## Greater Than or Equal To Operator

x5 = 10
y5 = 10

print("x5 >= y5 = ", x5 >= y5)

## Less Than or Equal To Operator

x6 = 10
y6 = 10

print("x6 <= y6 = ", x6 <= y6)

