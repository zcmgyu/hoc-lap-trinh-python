# Casting: Chuyển đổi kiểu dữ liệu

# int()
x1 = int("10")
print("type of x1 =", type(x1))
x2 = int(10.5)
print("x2 =", x2)
print("type of x2 =", type(x2))

# float()
y1 = float("10")
print("type of y1 =", type(y1))
y2 = float(10)
print("y2 =", y2)
print("type of y2 =", type(y2))

# str()
z1 = str(10)
print("type of z1 =", type(z1))
z2 = str(10.5)
print("type of z2 =", type(z2))
